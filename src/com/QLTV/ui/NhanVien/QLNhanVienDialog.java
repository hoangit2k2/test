    void filltable() {
        DefaultTableModel model = (DefaultTableModel) tbl.getModel();
        model.setRowCount(0);
        try {
            List<NhanVien> list = dao.selectAll();
            for (NhanVien NV : list) {
                Object[] row = {
                    NV.getNvID(),
                    NV.getHoTen(),
                    XDate.toString(NV.getNgaySinh(), "MM-dd-yyyy"),
                    NV.getString(),
                    NV.isChucVu() ? "Quản Lý" : "Nhân Viên",
                    NV.getDiaChi(),
                    NV.getEmail(),
                    NV.getSoDT(),
                    NV.getHinhAnh(),
                    NV.getThoiGian(), //System.out.println((char) NV.getThoiGian())
                };
                model.addRow(row);
            }
        } catch (Exception e) {
        }
    }

