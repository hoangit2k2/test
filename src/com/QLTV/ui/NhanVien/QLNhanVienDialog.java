    void setForm(NhanVien model) {
        JCalendar c = new JCalendar(model.getNgaySinh());
        txtchamcong.setText(model.getHoTen());
        txtmanv.setText(model.getNvID());
        txtmatkhau.setText(model.getString());
        txttenNV.setText(model.getHoTen());
        cldNgaySinh.setCalendar(c.getCalendar());
        txtdiachi.setText(model.getDiaChi());
        txtemail.setText(model.getEmail());
        txtsdt.setText(model.getSoDT());
        if (model.isChucVu() == true) {
            rdotp.setSelected(true);
        } else {
            rdonv.setSelected(true);
        }
//        rdonv.setSelected(model.isChucVu());
//        rdotp.setSelected(!model.isChucVu());
        if (model.getHinhAnh() != null) {
            lblanh.setToolTipText(model.getHinhAnh());
            lblanh.setIcon(XImage.read(model.getHinhAnh()));
        }
        txtwork.setText(String.valueOf(model.getThoiGian()));
    }

    NhanVien getModel() {
        Calendar calendar = this.cldNgaySinh.getCalendar();
        Date ngaysinh = calendar == null ? null
                : new Date(calendar.getTimeInMillis());
        NhanVien model = new NhanVien();
        model.setHoTen(txtchamcong.getText());
        model.setNvID(txtmanv.getText());
        model.setMatKhau(txtmatkhau.getText());
        model.setHoTen(txttenNV.getText());
        model.setNgaySinh(ngaysinh);
        model.setDiaChi(txtdiachi.getText());
        model.setSoDT(txtsdt.getText());
        model.setEmail(txtemail.getText());
        model.setChucVu(rdotp.isSelected());
        model.setHinhAnh(lblanh.getToolTipText());
        model.setThoiGian(Integer.valueOf(txtwork.getText()));
        return model;
    }

