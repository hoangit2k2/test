    void mani() {
        ChartPanel chartPanel = new ChartPanel(createChart());
        JDialog jdl = new JDialog(this, false);
        jdl.add(chartPanel);
        jdl.setSize(600, 400);
       jdl.setLocation(650, 270);
        jdl.setUndecorated(true);
        jdl.setVisible(true);
    }
    ThongKeLuotMuaDAO dao  = new ThongKeLuotMuaDAO();
    void filltable() {
        DefaultTableModel model = (DefaultTableModel) tbl.getModel();
        model.setRowCount(0);
        try {
            List<TKluotMua> list = dao.selectAll();
            for (TKluotMua TK : list) {
                Object[] row = {
                   TK.getThang(),
                   TK.getLuot()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
        }
    }

