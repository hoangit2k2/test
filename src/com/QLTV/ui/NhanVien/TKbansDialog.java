    public JFreeChart createChart() {
        JFreeChart barChart = ChartFactory.createBarChart(
                "Biếu Đồ Sách Bán Được",
                "Tháng", "Lượt Mua",
                createDataset(), PlotOrientation.VERTICAL, false, false, false);
        return barChart;
    }

