    ThongKeDAO dao = new ThongKeDAO();
      void filltable() {
        DefaultTableModel model = (DefaultTableModel) tbl.getModel();
        model.setRowCount(0);
        try {
            List<ThongKe> list = dao.selectAll();
            for (ThongKe TK : list) {
                Object[] row = {
                   TK.getTen(),
                   TK.getSL()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
        }
    }

