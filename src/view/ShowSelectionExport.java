    private DefaultTableModel model = null;

    private void exportToTxt() {
        model = (DefaultTableModel) this.table.getModel();
        Object[] arrs = model.getDataVector().toArray();
        String data = Util.createDocument(this.getName(), arrs);
        if (Util.ExportToTXT(data)) {
            Msg.Alert("Export to txt successfully!", "Export to excel: "
                    + table.getName(), Msg.MesType.ThongTin);
        } else {
            Msg.Alert("Export to txt failed!", "Export to txt: "
                    + table.getName(), Msg.MesType.ThongTin);
        }
    }

